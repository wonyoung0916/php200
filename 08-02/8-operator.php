<?php
    // 더하기
    $num = 5+5; // 5+5 를 더한값이 변수에 들어감
    echo "5+5는".$num;
    echo "<br>";
    // 빼기
    $num = 5-2; // 5-2값이 대입됨
    echo "5-2는".$num;
    echo "<br>";
    // 곱하기
    $num = 5*3; // 5*3값이 대입됨
    echo "5*3은".$num;
    echo "<br>";
    // 나누기
    $num = 6/3; // 6/3이 대입됨
    echo "6/3은".$num;
    echo "<br>";
    //나머지
    $num = 5%3; // 5%3이 대입됨
    echo "5%3은".$num;
    echo "<br>";
